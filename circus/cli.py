    def owner_keygen(self, args):
        """Generate owner keypair and insert into database."""
        import stat
        from datetime import datetime

        from circus.services.signing import generate_keypair, encode_public_key
        from circus.database import get_db

        output_path = Path(args.output)
        private_key_path = output_path
        public_key_path = output_path.with_suffix('.pub')

        # Check if files exist (refuse to overwrite unless --force)
        if not args.force:
            if private_key_path.exists():
                print(f"Error: Private key file already exists: {private_key_path}", file=sys.stderr)
                print("Use --force to overwrite", file=sys.stderr)
                sys.exit(1)
            if public_key_path.exists():
                print(f"Error: Public key file already exists: {public_key_path}", file=sys.stderr)
                print("Use --force to overwrite", file=sys.stderr)
                sys.exit(1)

        # Generate keypair
        private_bytes, public_bytes = generate_keypair()
        private_b64 = encode_public_key(private_bytes)  # reuse encoding function
        public_b64 = encode_public_key(public_bytes)

        # Write private key with 600 permissions
        private_key_path.write_text(private_b64 + '\n')
        private_key_path.chmod(0o600)

        # Write public key with 644 permissions
        public_key_path.write_text(public_b64 + '\n')
        public_key_path.chmod(0o644)

        # Insert into database
        now = datetime.utcnow().isoformat()
        with get_db() as conn:
            cursor = conn.cursor()
            cursor.execute("""
                INSERT OR REPLACE INTO owner_keys (owner_id, public_key, created_at, description)
                VALUES (?, ?, ?, ?)
            """, (args.owner, public_b64, now, args.description))
            conn.commit()

        print(f"✓ Owner keypair generated for '{args.owner}'")
        print(f"  Private key: {private_key_path} (mode 600)")
        print(f"  Public key: {public_key_path} (mode 644)")
        print(f"  DB row inserted: owner_keys.owner_id = {args.owner}")

    def owner_add(self, args):
        """Import owner public key from file (consuming agent path)."""
        from datetime import datetime

        from circus.database import get_db

        public_key_file = Path(args.public_key_file)
        if not public_key_file.exists():
            print(f"Error: Public key file not found: {public_key_file}", file=sys.stderr)
            sys.exit(1)

        # Read public key (expect single base64 line)
        public_b64 = public_key_file.read_text().strip()

        # Insert into database
        now = datetime.utcnow().isoformat()
        with get_db() as conn:
            cursor = conn.cursor()
            cursor.execute("""
                INSERT OR REPLACE INTO owner_keys (owner_id, public_key, created_at, description)
                VALUES (?, ?, ?, ?)
            """, (args.owner, public_b64, now, args.description))
            conn.commit()

        print(f"✓ Owner public key imported for '{args.owner}'")
        print(f"  Public key file: {public_key_file}")
        print(f"  DB row inserted: owner_keys.owner_id = {args.owner}")

    def preference_list(self, args):
        """List active preferences for an owner."""
        from circus.database import get_db

        owner_filter = args.owner if args.owner else None

        with get_db() as conn:
            cursor = conn.cursor()

            if owner_filter:
                cursor.execute("""
                    SELECT owner_id, field_name, value, effective_confidence, updated_at
                    FROM active_preferences
                    WHERE owner_id = ?
                    ORDER BY owner_id, field_name
                """, (owner_filter,))
            else:
                cursor.execute("""
                    SELECT owner_id, field_name, value, effective_confidence, updated_at
                    FROM active_preferences
                    ORDER BY owner_id, field_name
                """)

            rows = cursor.fetchall()

            if not rows:
                print("No active preferences found")
                return

            # Pretty print table
            print(f"\nActive Preferences ({len(rows)}):\n")
            print(f"{'Owner':<15} {'Field':<30} {'Value':<20} {'Confidence':<12} {'Updated':<20}")
            print("-" * 100)

            for row in rows:
                owner_id = row[0]
                field_name = row[1]
                value = row[2][:17] + "..." if len(row[2]) > 20 else row[2]
                confidence = f"{row[3]:.2f}"
                updated = row[4][:19] if row[4] else "N/A"

                print(f"{owner_id:<15} {field_name:<30} {value:<20} {confidence:<12} {updated:<20}")

    def preference_clear(self, args):
        """Clear (delete) a preference from active_preferences."""
        from circus.database import get_db

        owner = args.owner if args.owner else None
        field = args.field

        with get_db() as conn:
            cursor = conn.cursor()

            if owner:
                # Clear specific owner's preference
                cursor.execute("""
                    DELETE FROM active_preferences
                    WHERE owner_id = ? AND field_name = ?
                """, (owner, field))
            else:
                # Clear preference for all owners
                cursor.execute("""
                    DELETE FROM active_preferences
                    WHERE field_name = ?
                """, (field,))

            deleted = cursor.rowcount
            conn.commit()

            if deleted == 0:
                print(f"No preferences found for field '{field}'")
            else:
                print(f"✓ Cleared {deleted} preference(s) for field '{field}'")

