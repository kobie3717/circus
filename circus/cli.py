    pref_clear_parser = preference_subparsers.add_parser("clear", help="Clear a preference")
    pref_clear_parser.add_argument("field", help="Preference field name to clear")
    pref_clear_parser.add_argument("--owner", help="Owner ID (if omitted, clears for all owners)")

    # preference history
    pref_history_parser = preference_subparsers.add_parser("history", help="Show preference history")
    pref_history_parser.add_argument("--field", help="Filter by field name")
    pref_history_parser.add_argument("--owner", help="Filter by owner ID")

    # Allowlist command (Week 8)
    allowlist_parser = subparsers.add_parser("allowlist", help="Preference allowlist commands")
    allowlist_subparsers = allowlist_parser.add_subparsers(dest="allowlist_command", help="Allowlist subcommands")

    # allowlist list
    allowlist_list_parser = allowlist_subparsers.add_parser("list", help="List all allowlisted fields")

    # allowlist show
    allowlist_show_parser = allowlist_subparsers.add_parser("show", help="Show field details")
    allowlist_show_parser.add_argument("field", help="Field name to show")

    args = parser.parse_args()

    if not args.command:
        parser.print_help()
        sys.exit(1)

    # Initialize CLI
    cli = CircusCLI(base_url=args.base_url, token=args.token)

    # Execute command
    if args.command == "generate-passport":
        cli.generate_passport(args)
    elif args.command == "register":
        cli.register(args)
    elif args.command == "discover":
        cli.discover(args)
    elif args.command == "join":
        cli.join(args)
    elif args.command == "share":
        cli.share(args)
    elif args.command == "handshake":
        cli.handshake(args)
    elif args.command == "rooms":
        cli.rooms(args)
    elif args.command == "serve":
        cli.serve(args)
    elif args.command == "owner-keygen":
        cli.owner_keygen(args)
    elif args.command == "owner-add":
        cli.owner_add(args)
    elif args.command == "preference":
        if not hasattr(args, 'preference_command') or not args.preference_command:
            print("Usage: circus preference {list|clear|history} [options]", file=sys.stderr)
            sys.exit(1)
        elif args.preference_command == "list":
            cli.preference_list(args)
        elif args.preference_command == "clear":
            cli.preference_clear(args)
        elif args.preference_command == "history":
            cli.preference_history(args)
        else:
            print("Unknown preference subcommand", file=sys.stderr)
            sys.exit(1)
    elif args.command == "allowlist":
        if not hasattr(args, 'allowlist_command') or not args.allowlist_command:
            print("Usage: circus allowlist {list|show} [options]", file=sys.stderr)
            sys.exit(1)
        elif args.allowlist_command == "list":
            cli.allowlist_list(args)
        elif args.allowlist_command == "show":
            cli.allowlist_show(args)
        else:
            print("Unknown allowlist subcommand", file=sys.stderr)
            sys.exit(1)
    elif args.command == "hull":
        from circus.services.hull_integrity import (
            check_session, scan_session_dir, readiness_board, build_report
        )
        import json as _json
        if hasattr(args, "squadron") and args.squadron:
            reports = scan_session_dir(args.squadron, args.limit)
            if not reports:
                print("No JSONL session data found.", file=sys.stderr)
                sys.exit(1)
            print(readiness_board(reports))
            print(_json.dumps(reports, indent=2))
        elif hasattr(args, "session") and args.session:
            name = getattr(args, "agent_name", None) or "Agent"
            report = check_session(args.session, agent_name=name, token_limit=args.limit)
            if not report:
                print("No token usage data in session file.", file=sys.stderr)
                sys.exit(1)
            print(readiness_board([report]))
            print(_json.dumps(report, indent=2))
        elif hasattr(args, "tokens") and args.tokens is not None:
            name = getattr(args, "agent_name", None) or "Agent"
            report = build_report(name, args.tokens, args.limit)
            print(readiness_board([report]))
            print(_json.dumps(report, indent=2))
        else:
            print("Usage: circus hull --session <path> | --squadron <dir> | --agent <name> --tokens <N>")
            sys.exit(1)


